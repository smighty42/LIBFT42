int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	result;
	int	number;

	i = 0;
	sign = 1;
	result = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		sign = sign * -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = str[i] - '0';
		result = result * 10 + number;
		i++;
	}
	return (result * sign);
}
